package com.sskey.dms.model;

public class BalanceSum {
    private String id;
    private String saleDate;
    private String saleMan;
    private double totalBallance;
    private double inputDailyAmount;

    public BalanceSum() {
    }

    public BalanceSum(String id, String saleDate, String saleMan, double totalBallance, double inputDailyAmount) {
        this.id = id;
        this.saleDate = saleDate;
        this.saleMan = saleMan;
        this.totalBallance = totalBallance;
        this.inputDailyAmount = inputDailyAmount;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSaleDate() {
        return saleDate;
    }

    public void setSaleDate(String saleDate) {
        this.saleDate = saleDate;
    }

    public String getSaleMan() {
        return saleMan;
    }

    public void setSaleMan(String saleMan) {
        this.saleMan = saleMan;
    }

    public double getTotalBallance() {
        return totalBallance;
    }

    public void setTotalBallance(double totalBallance) {
        this.totalBallance = totalBallance;
    }

    public double getInputDailyAmount() {
        return inputDailyAmount;
    }

    public void setInputDailyAmount(double inputDailyAmount) {
        this.inputDailyAmount = inputDailyAmount;
    }
}
